// 19.8.0 Ок, мы разобрались с примитивными типами данных или массивами примитивных типов данных, но чуть сложнее будет сузить тип при работе с объектами или классами. Как один из вариантов, в них можно применить оператор "in", который проверит наличия свойства в объекте. Для примера создадим ещё одну функцию "checkReadings", у которой аргументом будет объект со свойством "system" с числовым типом данных и свойством "user", также с числовым типом данных. И вот теперь нам нужно сузить типы, в зависимости от свойства объекта. Если мы работаем с первым свойством, то мы выполняем одни действия, а если со вторым — то другие.
function checkReadings(readings: { system: number } | { user: number }): void {
  // 19.8.1 Ведь, если бы мы просто обратились к свойству "system", то TypeScript выдал бы ошибку, что возможно этого свойства не существует, т.к. мы раньше указали, что может быть либо "system", либо "user".
  // readings.system;

  // 19.8.2 Но, чтобы проверить, что такое свойство действительно существует в приходящем в функцию объекте используется оператор "in".
  if ("system" in readings) {
    console.log(readings.system);
  } else {
    console.log(readings.user);
  }
}

