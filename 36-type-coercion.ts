// 36.5.6 Переходим к созданию трансформирующей функции "transformDepartment", у которой будет один аргумент исходного объекта с аннотацией "Department", а также ещё второй аргумент, т.к. когда мы формируем новый объект у него создаётся новое свойство "projectBudget" с каким-то значением. Вот в этот второй аргумент и будет приходить кол-во денег для бюджета этого проекта и соответственно это будет число. А результатом будет объект, который должен подходить по структуре под интерфейс "Project", сразу укажем и это в аннотации функции.
// 36.5.7 Что будет делать эта функция: примет исходный объект, из которого нужно забрать какие-то данные, принимает также числовое значение для создания нового свойства "бюджет проекта", а результатом отдаст объект нужного формата только с нужными данными.
function transformDepartment(department: Department, amount: number): Project {
  // 36.5.8 Прописываем, что мы будем возвращать собственно объект только с теми данными, которые нужны по интерфейсу "Project".
  return {
    name: department.name,
    projectBudget: amount,
  };
}

// 36.5.9 Теперь мы можем закомментировать несовершенную версию объекта "mainProject" и пересоздать её здесь, используя новую функцию "transformDepartment". К слову, если мы здесь даже не будем указывать типом интерфейс "Project", то TypeScript всё равно определит его как "Project", т.к. мы указали этот интерфейс типом результата функции "transformDepartment".
const mainProject: Project = transformDepartment(department, 75000);

// 36.5.10 И вот теперь, если мы выведем в консоль "mainProject", то мы увидим объект только с нужными полями.
