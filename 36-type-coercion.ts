// 36.0.3 А ещё, если мы наведём мышку на переменную, в которой мы создаём числовой тип данных при помощи конструктора, то увидим, что TypeScript определил её тип как "Number", именно с большой буквы. Это специальный тип представляющий конструктор чисел (не путать с обычным "number").
