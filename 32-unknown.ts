// 32.5.0 Окей, перейдём к более практическим задачам. Рассмотрим два классических примера применения типа "unknown", которые довольно часто будут встречаться в реальных проектах.
// 32.5.1 Начнём с работы с методом JSON.parse, который может вернуть что угодно из JSON-строки. Мы рассматривали этот пример раньше и это исправляли жёсткой типизацией полученного объекта. А если мы даже не знаем, что это будет объект и у него будут свойства.
const userData = '{"isBirthdayData": true, "ageData": 40, "userNameData": "Jim"}';

// 32.5.2 Затем мы опишем функцию "safeParse". Мы скажем, что аргументом в неё придёт строка, а результатом будет тип "unknown". Потому, что мы и сами не знаем, что будет результатом парсинга этой JSON-строки.
function safeParse(data: string): unknown {
  return JSON.parse(data);
}

const data2 = safeParse(userData);

// 32.5.3 И теперь, раз уж у нас результат работы функции "safeParse" указан как "unknown", то мы уже не сможем применять на "data" какой-то случайный метод, как было в случае с "any".
// data2.someMethod();
// data2.push(3);

// 32.5.4 И тут, опять же, всё та же рекомендация по сужению типов, как было выше с получением неизвестных данных с серверного API.
function transferData(data: unknown): void {
  if (typeof data === "string") {
    console.log(data.toLowerCase());
  } else if (typeof data === "object" && data !== null) {
    // 32.5.5 Т.к. null может быть тоже объектом, то тут стоит дописать в условие, что data не является null.
    console.log(data);
  } else {
    console.error("Some error occurred while transferring data");
  }
}

transferData(data2);

// 35.6.0 Рассмотрим ещё один пример, связанный с объектами ошибок особенно в конструкции "try...catch". Для начала опишем две конструкции, где в одной "error" будет строкой, а в другой — объектом (т.к. мы создаём объект ошибки с помощью конструкции "new Error"). Но у них, как мы видим, типы ошибок разные и это зависит от реализации в блоке "try". TypeScript не может заранее знать, что за тип будет у "err".
try {
  if (1) throw 'error';
} catch (err) {
  console.log(typeof err); // string
}

try {
  if (1) throw new Error('error');
  // 35.6.1 Если навести на "err", то мы увидим, что TypeScript определил "err" типом "unknown".
} catch (err) {
  console.log(typeof err); // object
  // 35.6.2 И, если мы попытаемся обратиться, например к свойству "message" объекта ошибки, то TypeScript будет ругаться, что "err" у нас типа "unknown" и свойств может не иметь. Чтобы избежать этих TS-ошибок, тут также применяется метод сужения типов и чаще всего оператор "instanceof", чтобы определить, является ли экземпляр потомком какого-то класса (в данном случае мы использовали стандартный встроенный класс Error, но в разных проектах это может быть и специальный кастомный класс для логирования и вывода ошибок).
  if (err instanceof Error) {
  console.log(err.message);
  } else if (typeof err === "string") {
    console.log(err.trim());
  }
}

// 35.7.0 Ещё один важный аспект это работа с "union type" и "intersection type". Запишем два псевдонима типа "union type". Правило здесь такое: если "union type" содержит тип "unknown", то этот тип перекроет все остальные, кроме типа "any".
type T0 = any | unknown; // any
type T1 = number | unknown; // unknown

// 35.7.1 Создадим те же типы, но уже с оператором пересечения типов "intersection type". И мы увидим, что в таком случае тип "unknown" будет перекрываться другими типами.
type T2 = any & unknown; // any
type T3 = number & unknown; // number