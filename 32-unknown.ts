// ? 32.2.0 И вот, чтобы не прибегать к использованию опасного "any", мы можем использовать специальный тип "any", который укажет TypeScript, что "resp" — это пока неизвестный тип, а не любой (как в случае с "any").
// let resp: any;
let resp: unknown;

// 32.1.1 Затем в неё мы получим (якобы от сервера) какие-то данные строчного типа.
resp = "Some string from server API";

// 32.1.2 Также у нас есть данные в массиве строк "data", куда нам нужно поместить ответ от сервера "resp". Но это может быть что угодно.
// ? 32.2.1 Обратите внимание, что теперь TypeScript уже сигнализирует нам, что мы попытались поместить в "data" какой-то неизвестный тип данных (т.е. тип "unknown"). Вначале мы назвали тип "unknown" «безопасным аналогом "any"» потому, что к типу "any" мы можем применять абсолютно любой метод без каких-то ошибок от TypeScript, а к типу "unknow" не можем применить никакой метод, т.к. TypeScript скажет, что тип данных ещё неизвестен, что предохранит нас от возможных runtime ошибок.
// ? 32.2.2 Данные с типом "unknown" можно поместить лишь в переменную с ещё одним "unknow" или типом "any". ↓
// const data: string[] = resp;

// 32.1.3 И вот, здесь мы имеем уже поле для ошибок, т.к. думая, что имеем дело с массивом мы можем применить к data метод для массивов и TypeScript не предупредит нас. Вот почему опасно и не рекомендуется использовать тип "any" — в действительности он нам не помогает, но может даже подставить под ошибки. ↑
// data.find(elem => console.log(elem));

// 32.3.0 Создадим ещё один пример, чтобы показать разницу между типами "unknown" & "any".
const someValue: any = 101;

// 32.3.1 Теперь попробуем вызвать на этой переменной какой-то случайный метод. И TypeScript закономерно молчит, т.к. он понятия не имеет, что за тип данных в этой "someValue".
// someValue.someMethod();

// 32.3.2 Попробуем провернуть всё то же самое, но с типом "unknown". И вот TypeScript уже ругается, что мы не можем применять методы на переменных с неизвестным типом данных. Как видно на этих примерах, использование "unknown" является куда более безопасным и потому предпочтительным для работы с неизвестными типами данных.
const someUnknownValue: unknown = 5;

